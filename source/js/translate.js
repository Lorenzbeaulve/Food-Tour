function setLanguage(lang) { // aggiorna testi tramite ID
    document.querySelectorAll("[id]").forEach(el => {
        const key = el.id;
        if (translations[lang] && translations[lang][key] !== undefined) {
            if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") return;
            el.textContent = translations[lang][key];
        }
    });
    // aggiorna stile bottoni
    document.querySelectorAll(".lang-btn").forEach(btn => btn.classList.remove("active"));
    document.querySelector(`button[onclick="setLanguage('${lang}')"]`).classList.add("active");
    localStorage.setItem("lang", lang); // memorizza la lingua scelta
}

document.addEventListener("DOMContentLoaded", () => { // imposta automaticamente la lingua salvata o di default
    const savedLang = localStorage.getItem("lang") || "it";
    setLanguage(savedLang);
});

// Utility per ottenere la lingua corrente e traduzioni singole
function getLanguage() {
    return localStorage.getItem('lang') || 'it';
}

function getTranslation(key) {
    const lang = getLanguage();
    if (translations[lang] && translations[lang][key] !== undefined) return translations[lang][key];
    return key; // fallback: ritorna la chiave stessa
}


